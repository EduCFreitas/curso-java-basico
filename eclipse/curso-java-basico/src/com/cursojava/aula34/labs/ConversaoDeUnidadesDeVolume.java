package com.cursojava.aula34.labs;

public class ConversaoDeUnidadesDeVolume {

	public static double converterLitroParaCentimetroCubico(double volume) {
		return volume*1000;
	}
	
	public static double converterMetroCubicoParaLitro(double volume) {
		return volume*1000;
	}
	
	public static double converterMetroCubicoParaPeCubico(double volume) {
		return volume*35.32;
	}
	
	public static double converterGalaoAmericanoParaPolegadaCubica(double volume) {
		return volume*231;
	}
	
	public static double converterGalaoAmericanoParaLitro(double volume) {
		return volume*3.785;
	}
}
