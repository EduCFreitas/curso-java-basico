package com.cursojava.aula34.labs;

public class ConversaoDeUnidadesDeTempo {

	public static double converterMinutoParaSegundo(double tempo) {
		return tempo*60;
	}
	
	public static double converterHoraParaMinuto(double tempo) {
		return tempo*60;
	}
	
	public static double converterDiaParaHora(double tempo) {
		return tempo*24;
	}
	
	public static double converterSemanaParaDia(double tempo) {
		return tempo*7;
	}
	
	public static double converterMesParaDia(double tempo) {
		return tempo*30;
	}
	
	public static double converterAnoParaDia(double tempo) {
		return tempo*365.25;
	}
}
